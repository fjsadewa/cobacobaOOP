// button Login
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        PreparedStatement st;
        ResultSet rs;
        
        // mengambil input
        String UsernameP = jTextField_usernameP.getText();
        String PasswordP = String.valueOf(jPasswordField_Penumpang.getPassword());
        
        // mengambil data dari database
        String query = "SELECT * FROM `data_penumpang` WHERE `UsernameP` = ? AND `PasswordP` = ?";
        
        try {
            st = DBConnection.getConnection().prepareStatement(query);
            
            st.setString(1, UsernameP);
            st.setString(2, PasswordP);
        
            rs = st.executeQuery();
            
            // jika benar berpindah halaman ke pemesanan tiket
            if(rs.next()){
                Medicine Med = new Medicine();
                Med.setVisible(true);
                Med.pack();
                Med.setLocationRelativeTo(null);
                this.dispose();
            }
            else{
                JOptionPane.showMessageDialog(null, "Invalid username and password");
            }
        } catch (SQLException ex) {
            Logger.getLogger(LoginAdmin.class.getName()).log(Level.SEVERE, null, ex);
        }
       
    }//GEN-LAST:event_jButton1ActionPerformed

