// Menghapus tulisan hint pada username
    private void jTextField_usernamePFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextField_usernamePFocusGained
        if (jTextField_usernameP.getText().trim().equals("sukalupa")){
            jTextField_usernameP.setText("");
            jTextField_usernameP.setForeground(Color.black);
        }
    }//GEN-LAST:event_jTextField_usernamePFocusGained
// Menampilkan tulisan hint pada username
    private void jTextField_usernamePFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextField_usernamePFocusLost
        if(jTextField_usernameP.getText().trim().equals("")||
           jTextField_usernameP.getText().trim().equals("sukalupa")){
                jTextField_usernameP.setText("sukalupa");
                jTextField_usernameP.setForeground(new Color(153,153,153));
        }
    }//GEN-LAST:event_jTextField_usernamePFocusLost

