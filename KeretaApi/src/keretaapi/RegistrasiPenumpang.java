// cek Kesamaan Username
    public boolean CheckUsername (String UsernameP){
        PreparedStatement st;
        ResultSet rs;
        
        boolean User_sama = false;
        
        String query = "SELECT * FROM `data_penumpang` WHERE `UsernameP` = ?";
        
        try {
            st = DBConnection.getConnection().prepareStatement(query);
            st.setString(1, UsernameP);
            rs = st.executeQuery();
            // jika username sama
            if (rs.next()){
                User_sama = true;
                jError.setText("Username Already Taken!");
                jError.setForeground(Color.red);
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(RegistrasiPenumpang.class.getName()).log(Level.SEVERE, null, ex);
        }
        return User_sama;
    }

// cek kolom kosong
    public boolean cekKolom(){
        String Usname = jTextField_Username.getText();
        String Pass = String.valueOf(jPasswordField.getPassword());
        String NLengkap = jTextField_Username.getText();
        String Eml = jTextField_Email.getText();
        String Tgl = jFormattedTextField_Date.getText();
        String Tlp = jTextField_Telp.getText();
        
        if (Usname.trim().equals("")||Pass.trim().equals("")||NLengkap.trim().equals("")||Eml.trim().equals("")
            ||Tgl.trim().equals("")||Tlp.trim().equals("")){
            JOptionPane.showMessageDialog(null,"Kotak tidak boleh kosong");
            return false;
        }
    return true;
    }
    
