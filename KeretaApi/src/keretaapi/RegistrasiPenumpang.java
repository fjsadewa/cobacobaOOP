//submit registrasi
    private void jButton_RegistActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_RegistActionPerformed
        //Getter -> menerima hasil input
        String NamaP = jTextField_FullName.getText();
        String UsernameP = jTextField_Username.getText();
        String EmailP = jTextField_Email.getText();
        String PasswordP =  String.valueOf(jPasswordField.getPassword());
        String Gender = "Laki-laki";
        if (jRadioButton_Female.isSelected()) Gender = "Female";
        String Telp = jTextField_Telp.getText();
       
        if (cekKolom()){
            if (!CheckUsername(UsernameP)){
                PreparedStatement ps;
                ResultSet rs;
                
                // menginputkan Data ke dalam Database
                String Regist_query = "INSERT INTO `data_penumpang`(`UsernameP`, `PasswordP`, `NamaLengkapP`, `EmailP`, `JenisKelamin`, `Telp`) VALUES (?,?,?,?,?,?)";    
            
                try {
                    ps = DBConnection.getConnection().prepareStatement(Regist_query);
                    ps.setString(1, UsernameP);
                    ps.setString(2, PasswordP);
                    ps.setString(3, NamaP);
                    ps.setString(4, EmailP);
                    ps.setString(5, Gender);
                    ps.setString(6, Telp);
                //jika berhasil
                if (ps.executeUpdate() != 0)
                    JOptionPane.showMessageDialog(null, "Berhasil! Silahkan kembali ke Login");
                //jika gagal
                else JOptionPane.showMessageDialog(null, "yah pembuatan akun Gagal :("); 

                } catch (SQLException ex) {
                    Logger.getLogger(RegistrasiPenumpang.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_jButton_RegistActionPerformed

